<template>
	<div class="itemWarp flex_mid" @click='changePage'>
		<span v-show='!bol'>
			<slot name='normalImg'></slot>
                <i :class="icon"></i>

		</span>
		<span v-show='bol'>
			<slot name='activeImg'></slot>
                <i :class="icon" class="acitve"></i>
		</span>
		<span v-text='txt'></span>
	</div>
</template>
<script type="text/javascript">
	export default{
		name: 'Item',
		props:{
			txt:{
				type:String
			},
			page:{
				type:String
			},
			sel:{
				type:String
            },
			icon:{},
			isShow:{
				type:Boolean
			}
		},
		computed:{
			bol: function(){
				if(this.sel == this.page){
					return true;
				}
				return false;
			}
		},
		methods:{
			changePage:function(){
				//点击跳转对应的页面
				this.$store.commit('changeTabactive',this.isShow)
				this.$router.push('/'+this.page);
				this.$emit('change',this.page)
			}
		}
	}
</script>
